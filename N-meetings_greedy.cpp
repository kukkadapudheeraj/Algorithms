class Solution
{
    public:
    //Function to find the maximum number of meetings that can
    //be performed in a meeting room.
    int maxMeetings(int start[], int end[], int n)
    {
        // Your code here
        pair<int,int> pr[n];
        for(int i=0;i<n;i++)
        {
            pr[i].first=end[i];
            pr[i].second=start[i];
        }
        sort(pr,pr+n);
        int c=1;
        int en=pr[0].first;
        for(int i=1;i<n;i++)
        {
            if(pr[i].second>en)
            {
                c++;
                en=pr[i].first;
            }
        }
        return c;
    }
};

